package com.meta.java8;

public class Employee1 {

	private String name;
	private String gender;
	private String city;

	public Employee1(String name, String gender, String city) {

		this.name = name;
		this.gender = gender;
		this.city = city;

	}

	public String getName() {
		return name;
	}

	public String getGender() {
		return gender;
	}

	public String getCity() {
		return city;
	}

}
