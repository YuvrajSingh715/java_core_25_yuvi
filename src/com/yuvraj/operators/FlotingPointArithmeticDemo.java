package com.yuvraj.operators;

public class FlotingPointArithmeticDemo {

public static void main(String[] args) {
		
		System.out.println( 10/0.0);
		
		System.out.println(-10/0.0);
		
		System.out.println(0/0.0);
		
		System.out.println(0.0/0);
		
		System.out.println(Math.sqrt(16));
		System.out.println(Math.sqrt(-16));

	}

	
}
